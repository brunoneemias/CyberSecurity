# Leitura do número
numero = int(input("Digite um número inteiro: "))

# Verificação e exibição do resultado
print("Você digitou:", numero)

if numero == 0:
    print("O número é nulo.")
if numero < 0:
    print("O número é negativo.")
if numero > 0:
    print("O número é positivo.")
